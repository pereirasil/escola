export default function BottomNav({ items, onMenuClick }) {
  const linkClass = ({ isActive }) =>
    `bottom-nav-item${isActive ? ' bottom-nav-item-active' : ''}`

  return (
    <nav className="bottom-nav" role="navigation" aria-label="Menu principal">
      <div className="bottom-nav-inner">
        {items.map((item) => {
          if (item.type === 'button') {
            return (
              <button
                key={item.key}
                type="button"
                className="bottom-nav-item"
                onClick={onMenuClick}
                aria-label="Abrir menu completo"
              >
                <span className="bottom-nav-icon">{icons[item.icon] || icons.menu}</span>
                <span className="bottom-nav-label">{item.label}</span>
              </button>
            )
          }
          return (
            <NavLink
              key={item.key}
              to={item.to}
              className={linkClass}
              end={item.end}
            >
              <span className="bottom-nav-icon">{icons[item.icon] || icons.menu}</span>
              <span className="bottom-nav-label">{item.label}</span>
            </NavLink>
          )
        })}
      </div>
      <div className="bottom-nav-safe" aria-hidden="true" />
    </nav>
  )
}
